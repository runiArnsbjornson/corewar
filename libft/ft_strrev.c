char	*ft_strrev(char *str)
{
	char	*p1;
	char	*p2;
	char	tmp;

	p1 = str;
	p2 = str;
	while (*p2 != '\0')
		++p2;
	while (--p2 > p1)
	{
		tmp = *p1;
		*p1++ = *p2;
		*p2 = tmp;
	}
	return (str);
}
