#include "libft.h"

int	ft_isint(char *str)
{
	size_t	len;

	len = ft_strlen(str);
	if (*str == '+' || *str == '-')
	{
		++str;
		if (*str == '\0')
			return (0);
	}
	while (*str != '\0')
		if (FT_ISDIGIT(*str++) == 0)
			return (0);
	return (1);
}
